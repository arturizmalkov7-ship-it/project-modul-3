drop policy if exists profiles_select_policy on public.profiles;
create policy profiles_select_policy on public.profiles
for select
to authenticated
using (
  public.is_active_user()
  and (public.is_admin() or id = auth.uid())
);

drop policy if exists profiles_update_self_policy on public.profiles;
create policy profiles_update_self_policy on public.profiles
for update
to authenticated
using (public.is_active_user() and id = auth.uid())
with check (
  id = auth.uid()
  and role = (select role from public.profiles where id = auth.uid())
  and is_active = (select is_active from public.profiles where id = auth.uid())
);

drop policy if exists profiles_update_admin_policy on public.profiles;
create policy profiles_update_admin_policy on public.profiles
for update
to authenticated
using (public.is_active_user() and public.is_admin())
with check (public.is_active_user() and public.is_admin());

